-- 6. Create subcategories table
CREATE TABLE IF NOT EXISTS public.service_subcategories (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  category_id uuid NOT NULL REFERENCES service_categories(id) ON DELETE CASCADE,
  name text NOT NULL,
  description text,
  icon text,
  is_active boolean DEFAULT true,
  created_at timestamptz DEFAULT now()
);

ALTER TABLE public.service_subcategories ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Anyone can read subcategories"
  ON public.service_subcategories FOR SELECT
  USING (true);

CREATE POLICY "Admins manage subcategories"
  ON public.service_subcategories FOR ALL
  TO authenticated
  USING (public.has_role(auth.uid(), 'super_admin'::public.user_role));

